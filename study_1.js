// outputting a message or string
console.log("Hello World!");

//addition, subtraction, multiplication, division
console.log(5 + 5);
console.log(10 - 3);
console.log(5 * 5);
console.log(50 / 5);

//string concatenation
console.log("My name is " + "Brian");

// declaring a variable using 'let'
let myName = "Brian";

console.log(myName);

//variable names can be changed without using 'let' again
myName = "Tracy";
console.log(myName);

//constant
//constants cannot be changed as opposed to variables
const vehicleBrand = "Toyota";
console.log("The vehicle you selected is a " + vehicleBrand);

//template literals
const country = "Malaysia";

//entire string is enclosed with backticks
console.log(`Hello ${myName} and welcome to ${country}`);
